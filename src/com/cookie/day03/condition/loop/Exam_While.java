package com.cookie.day03.condition.loop;

public class Exam_While {
	public static void main(String[] args) {
		/*
		 * 제어문-반복문
		 * 2.while
		 * 문법
		 * 초기식;
		 * while(조건식) {실행문장; 증감식;}
		 * 
		 * for(초기식; 조건식; 증감식) {실행문장;}
		 *문제1-1
		 */
		//1부터 10가지 출력
		int i=1;
		while( i<=10) {
			System.out.println(i);
			i++;
		}
		//문제1-2
		//while문을 이용하여 1~100사이의 홀수의 합을 출력
	}
}
